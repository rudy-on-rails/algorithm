require 'set'
def remove_covered_intervals(intervals)
  initial = intervals.count
  covered = Set.new
  intervals.each.with_index do |interval, index|
    intervals[index+1..].each do |interval2|
      if is_covered?(interval, interval2)
        covered << interval
      elsif is_covered?(interval2, interval)
        covered << interval2
      end
    end
  end
  initial - covered.count
end

def is_covered?(i1, i2)
  i1[0] >= i2[0] && i1[1] <= i2[1]
end

